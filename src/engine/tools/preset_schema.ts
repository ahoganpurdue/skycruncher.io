// â”€â”€â”€ TYPES â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

export interface ToneCurvePoint {
  /** Input value (0-255) */
  x: number;
  /** Output value (0-255) */
  y: number;
}

export interface HSLChannel {
  /** Hue shift (-180 to +180) */
  hue: number;
  /** Saturation adjustment (-100 to +100) */
  saturation: number;
  /** luminance adjustment (-100 to +100) */
  luminance: number;
}

export interface EditRecipe {
  /** Preset name */
  name: string;
  /** Description */
  description: string;

  // â”€â”€ Global Adjustments â”€â”€
  /** Exposure compensation (-5.0 to +5.0) */
  exposure: number;
  /** Contrast (-100 to +100) */
  contrast: number;
  /** Highlights recovery (-100 to +100) */
  highlights: number;
  /** Shadows fill (-100 to +100) */
  shadows: number;
  /** Whites clipping (-100 to +100) */
  whites: number;
  /** Blacks clipping (-100 to +100) */
  blacks: number;
  /** Texture (midtone detail, -100 to +100) */
  texture: number;
  /** Clarity (midtone contrast, -100 to +100) */
  clarity: number;
  /** Dehaze (-100 to +100) */
  dehaze: number;
  /** Vibrance (-100 to +100) */
  vibrance: number;
  /** Saturation (-100 to +100) */
  saturation: number;

  // â”€â”€ White balance â”€â”€
  /** Color temperature (2000-50000 Kelvin) */
  temperature: number;
  /** Tint (green-magenta, -150 to +150) */
  tint: number;

  // â”€â”€ Tone Curve â”€â”€
  /** Parametric tone curve points */
  tone_curve: ToneCurvePoint[];

  // â”€â”€ HSL / Color â”€â”€
  /** Per-color HSL adjustments */
  hsl: {
    red: HSLChannel;
    orange: HSLChannel;
    yellow: HSLChannel;
    green: HSLChannel;
    aqua: HSLChannel;
    blue: HSLChannel;
    purple: HSLChannel;
    magenta: HSLChannel;
  };

  // â”€â”€ Detail â”€â”€
  /** Sharpening amount (0-150) */
  sharpening: number;
  /** Sharpening radius (0.5-3.0) */
  sharpening_radius: number;
  /** luminance noise reduction (0-100) */
  noise_reduction_luminance: number;
  /** Color noise reduction (0-100) */
  noise_reduction_color: number;

  // â”€â”€ Lens Corrections â”€â”€
  /** Enable profile-based lens corrections */
  lens_correction_enabled: boolean;
  /** Vignette correction amount (0-200, 100=full) */
  vignette_correction: number;
  /** Distortion correction amount (0-200, 100=full) */
  distortion_correction: number;
}

// â”€â”€â”€ DEFAULTS â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

export function createDefaultRecipe(name: string = 'Untitled'): EditRecipe {
  const defaultHSL: HSLChannel = { hue: 0, saturation: 0, luminance: 0 };
  return {
    name,
    description: 'Generated by SKYCRUNCHER Preset Developer',
    exposure: 0, contrast: 0, highlights: 0, shadows: 0,
    whites: 0, blacks: 0, texture: 0, clarity: 0, dehaze: 0,
    vibrance: 0, saturation: 0,
    temperature: 6500, tint: 0,
    tone_curve: [
      { x: 0, y: 0 }, { x: 64, y: 64 },
      { x: 128, y: 128 }, { x: 192, y: 192 },
      { x: 255, y: 255 },
    ],
    hsl: {
      red: { ...defaultHSL }, orange: { ...defaultHSL },
      yellow: { ...defaultHSL }, green: { ...defaultHSL },
      aqua: { ...defaultHSL }, blue: { ...defaultHSL },
      purple: { ...defaultHSL }, magenta: { ...defaultHSL },
    },
    sharpening: 40, sharpening_radius: 1.0,
    noise_reduction_luminance: 0, noise_reduction_color: 25,
    lens_correction_enabled: true,
    vignette_correction: 100, distortion_correction: 100,
  };
}

