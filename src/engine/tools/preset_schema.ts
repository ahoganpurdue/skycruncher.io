/**
 * Generate a valid Adobe Camera Raw .xmp preset file.
 *
 * This output can be:
 * 1. Saved as a .xmp file in the Lightroom presets folder
 * 2. Imported via Lightroom's "Import Presets" function
 * 3. Applied via Adobe Camera Raw
 */
